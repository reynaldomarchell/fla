package main;

//Student class
class Student {
	 private String name;
	 private double testScore;
	 private double homeworkScore;
	 private double projectScore;
	 private double essayScore;
	 private double participationScore;
	 private double presentationScore;
	 
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public double getTestScore() {
		return testScore;
	}
	public void setTestScore(double testScore) {
		this.testScore = testScore;
	}
	public double getHomeworkScore() {
		return homeworkScore;
	}
	public void setHomeworkScore(double homeworkScore) {
		this.homeworkScore = homeworkScore;
	}
	public double getProjectScore() {
		return projectScore;
	}
	public void setProjectScore(double projectScore) {
		this.projectScore = projectScore;
	}
	public double getEssayScore() {
		return essayScore;
	}
	public void setEssayScore(double essayScore) {
		this.essayScore = essayScore;
	}
	public double getParticipationScore() {
		return participationScore;
	}
	public void setParticipationScore(double participationScore) {
		this.participationScore = participationScore;
	}
	public double getPresentationScore() {
		return presentationScore;
	}
	public void setPresentationScore(double presentationScore) {
		this.presentationScore = presentationScore;
	}
	 
}
